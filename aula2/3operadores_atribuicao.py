print(saldo)
saldo += 200 # "+=" atribuicao com adicao
print(saldo)
saldo -= 200 # "-=" atribuicao com subtracao
print(saldo)
saldo *= 2 # "*=" atribuicao com multiplicacao
print(saldo)
saldo /= 2 # "/=" atribuicao com divisao
print(saldo)
saldo %= 480 # "%=" atribuicao com modulo
