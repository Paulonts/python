print(curso.lower()) # deixa tudo minusculo
print(curso.title()) # deixa a primira letra em maiuscula
print("\n")


curso = "          python   "
print(curso.strip()) # tira o espaço do da esquerda e da direita
print(curso.lstrip()) # tira o espaço da esquerda
print(curso.rstrip()) # tira o espaço da direita
print("\n")


curso = "python"
